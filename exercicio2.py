#2 - Um usuário precisar saber o maior entre dois números digitados.Desenvolva o algoritmo que resolva isso.

numero1 = int (input("digite o primeiro numero: "))
numero2 = int (input("digite o segundo numero: "))

if numero1 > numero2 :
    print(f"o seu {numero1} é maior que {numero2}")
else :
    print(f"o numero {numero2} é maior que o numero 8{numero1}")